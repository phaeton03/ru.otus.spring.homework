DROP TABLE IF EXISTS AUTHOR;

CREATE TABLE AUTHOR
(
    ID IDENTITY PRIMARY KEY,
    NAME VARCHAR(255)
);

DROP TABLE IF EXISTS GENRE;

CREATE TABLE GENRE
(
    ID IDENTITY PRIMARY KEY,
    NAME VARCHAR(255)
);

DROP TABLE IF EXISTS BOOKS;

CREATE TABLE BOOKS
(
    ID IDENTITY PRIMARY KEY,
    NAME      VARCHAR(255),
    AUTHOR_ID BIGINT,
    GENRE_ID  BIGINT,
    FOREIGN KEY (AUTHOR_ID)
        REFERENCES AUTHOR (ID)
        ON DELETE CASCADE,
    FOREIGN KEY (GENRE_ID)
        REFERENCES GENRE (ID)
        ON DELETE CASCADE
);